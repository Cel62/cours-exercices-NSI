def affiche_grille():
    '''
    Fonction qui affiche graphiquement une grille
    '''
    for ligne in range(hauteur_grille(grille)):
        for colonne in range(largeur_grille(grille)):
            if grille[ligne][colonne] == 1:
                color = '#111111'
            else:
                color = '#AAAAAA'
            Terrain.itemconfigure(Carreau[ligne][colonne], fill = color)

def apres():
    global grille
    global anim_id
    grille = generation_suivante(grille)
    affiche_grille()
    anim_id = fenetre.after(100, apres)    # après une durée donnée en ms, on relance
    
def stop():
    fenetre.after_cancel(anim_id)
    fenetre.destroy()

